/**
  * @brief This function handles TIM7 global interrupt.
  */
void TIM7_IRQHandler(void)
{
  /* USER CODE BEGIN TIM7_IRQn 0 */

//**************************************************************************************
// Rich Shield blue LED dimming example PVT2021
// Uses reprogramming the timer value from within the timer interrupt routine
// in order to control when the next timer interrupt is coming.
//
// A LED can also be dimmed using hardware PWM, but this was not the point here.
//**************************************************************************************


 static int ontime=32738; // halfway counter range, duty cycle 0.5  LED lights pretty much similar to full on state there
 static int dimming_increment=100; //controls the period of the on/off cycles

  /* USER CODE END TIM7_IRQn 0 */
  HAL_TIM_IRQHandler(&htim7);
  /* USER CODE BEGIN TIM7_IRQn 1 */
  HAL_GPIO_TogglePin(GPIOB,GPIO_PIN_1); //blue LED on Rich Shield

  if (HAL_GPIO_ReadPin(GPIOB,GPIO_PIN_1)==1)
  	  {
	  //LED is on here
  	  __HAL_TIM_SET_COUNTER(&htim7, 65535-ontime); //set counter value; it overflows past 65535
  	  }
  else
	  {
	  //LED is off here
	  __HAL_TIM_SET_COUNTER(&htim7, ontime); //set counter value; it overflows past 65535
	  }

  ontime+=dimming_increment;

  if ((ontime>33000)||(ontime<200)) //operating range adjusted for visual dimming perception
  {
	  dimming_increment = - dimming_increment;
  }


  /* USER CODE END TIM7_IRQn 1 */
}

